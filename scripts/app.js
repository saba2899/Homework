let food = "Pizza";

switch (food) {
    case "Pizza":
        console.log("თქვენი პიცა მზადდება");
        break;
    case "Burger":
        console.log("თქვენი ბურგერი მზადდება");
        break;
    case "Pasta":
        console.log("თქვენი მაკარონი მზადდება");
        break;
        default:
            console.log("ასეთი კერძი არ გვაქვს");
}